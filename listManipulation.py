## List manipulation
x = [1,2,3,4,5,3,21,2]

# Adds to the end of the list
x.append(2)

# insert takes to parameters where index value and value 
x.insert(1, 2)
# put two to the first index value

x.remove(x[2])
# or 
x.remove(2) # removes first two in list

# Returns index value of 1 in the list
x.index(1)

# Counts the number of 6's in the list
x.count(6)

# sort is a function run on x. 
# returns a sorted list either alpha or num
x.sort()

y = ["Janet", "Jessy", "Bill", "Jack"]

y.sort()

